/* A. WHAT IS A WEN API?
  1. API stands for Application Programming Interface. A Web API is an application programming interface 
     for the Web.

  2. A Browser API can extend the functionality of a web browser. A Server API can extend the function-
     ality of a web server.

  3. All browsers have a set of built-in Web APIs to support complex operations, and to help accessing
     data. For example, the Geolocation API can return the coordinates of where the browser is located.

  4. Third party APIs are not built into your browser. To use these APIs, you will have to download 
     the code from the Web. Examples:
     - YouTube API - Allows you to display videos on a web site.
     - Twitter API - Allows you to display Tweets on a web site.
     - Facebook API - Allows you to display Facebook info on a web site. */